package exercises.Conditionals;

public class Main {

	public static void main(String[] args) {
	
	int sayi = 25;
	
	if (sayi > 25) {
		System.out.println("Sayı 25'ten büyüktür");
		}
	else if (sayi == 25) {
		System.out.println("Sayı 25'e eşittir");
	}
	else {
		System.out.println("Sayı 25'ten küçüktür");
	}

	}

}
